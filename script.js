// Profile image interactions
function initProfileImage() {
    const profileImage = document.querySelector('.profile-image');
    const profilePhoto = document.querySelector('.profile-photo');
    
    if (!profileImage || !profilePhoto) return;
    
    // Add subtle mouse move effect
    profileImage.addEventListener('mousemove', function(e) {
        const rect = profileImage.getBoundingClientRect();
        const x = e.clientX - rect.left;
        const y = e.clientY - rect.top;
        
        const centerX = rect.width / 2;
        const centerY = rect.height / 2;
        
        // Very subtle movement - no rotation, just gentle translation
        const moveX = Math.max(-5, Math.min(5, (x - centerX) / 30));
        const moveY = Math.max(-5, Math.min(5, (y - centerY) / 30));
        
        profilePhoto.style.transform = `translate(${moveX}px, ${moveY}px) translateZ(0)`;
    });
    
    profileImage.addEventListener('mouseleave', function() {
        profilePhoto.style.transform = 'translate(0px, 0px) translateZ(0)';
    });
    
    // Add gentle click effect
    profileImage.addEventListener('click', function() {
        // Gentle pulse effect on click
        profileImage.style.transform = 'scale(0.98) translateZ(0)';
        setTimeout(() => {
            profileImage.style.transform = 'scale(1) translateZ(0)';
        }, 150);
    });
}

